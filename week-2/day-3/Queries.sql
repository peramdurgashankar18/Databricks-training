-- day 3: number manipulation practice queries

-- question 1: round 3.14159 to 2 decimal places
round(3.14159, 2)
-- answer: 3.14

-- question 2: round 3.5 to the nearest integer
round(3.5, 0)
-- answer: 4 (or 3 depending on rounding mode)

-- question 3: round up 3.2 to the nearest integer (CEIL)
ceil(3.2)
-- answer: 4

-- question 4: round down 3.8 to the nearest integer (FLOOR)
floor(3.8)
-- answer: 3

-- question 5: get the absolute value of -42
abs(-42)
-- answer: 42

-- question 6: find the remainder when 17 is divided by 5 (MOD)
mod(17, 5)
-- answer: 2

-- question 7: calculate 2 raised to the power of 5
power(2, 5)
-- answer: 32

-- question 8: find the square root of 144
sqrt(144)
-- answer: 12

-- question 9: calculate e raised to the power of 2
exp(2)
-- answer: 7.38905609893065

-- question 10: find the natural logarithm of 10
ln(10)
-- answer: 2.302585092994046

-- question 11: find the logarithm base 10 of 1000
log10(1000)
-- answer: 3

-- question 12: check the sign of -100 (should return -1)
sign(-100)
-- answer: -1

-- question 13: check the sign of 50 (should return 1)
sign(50)
-- answer: 1

-- question 14: find the greatest value among 45, 120, 78
greatest(45, 120, 78)
-- answer: 120

-- question 15: find the smallest value among 45, 120, 78
least(45, 120, 78)
-- answer: 45

-- question 16: truncate 9.876 to 1 decimal place
truncate(9.876, 1)
-- answer: 9.8

-- question 17: calculate percentage - 25% of 500
(25 * 500) / 100
-- answer: 125

-- question 18: calculate average of multiple numbers - (10 + 20 + 30) / 3
(10 + 20 + 30) / 3
-- answer: 20

-- question 19: round the result of 7 divided by 3 to 2 decimal places
round(7.0 / 3, 2)
-- answer: 2.33

-- question 20: find the difference between 100 and -50, then get absolute value
